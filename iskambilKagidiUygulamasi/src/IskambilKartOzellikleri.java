
public class IskambilKartOzellikleri {
	
	
	
	private String sayi;
	private String grup;
	
	
	
	public IskambilKartOzellikleri(String sayi, String grup) {
		
		this.sayi = sayi;
		this.grup = grup;
	}
	public String getSayi() {
		return sayi;
	}
	public void setSayi(String sayi) {
		this.sayi = sayi;
	}
	public String getGrup() {
		return grup;
	}
	public void setGrup(String grup) {
		this.grup = grup;
	}
	
	

}
